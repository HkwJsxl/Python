"""
小明单位发了100元的购物卡，小明到超市买三类洗化用品：
    洗发水（15元）、香皂（2元）、牙刷（5元）。要把100元正好花掉，可有哪些购买组合？
"""

money = 100
n = money // 15  # 最多可买洗发水个数
for i in range(n, -1, -1):  # (n+1)
    m = (money - i * 15) // 5  # 最多可买牙刷个数
    for j in range(m, -1, -1):  # (m+1)
        k = (money - i * 15 - j * 5) // 2  # 最多可买香皂个数
        # if (money - i * 15 - j * 5) % 2 == 0:
        if money == i * 15 + j * 5 + k * 2:
            print('可选择的购买组合:\t\t购买洗发水 {} 瓶，牙刷 {}个，香皂 {} 块。'.format(i, j, k))
